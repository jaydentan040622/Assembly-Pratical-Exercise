.MODEL SMALL
.STACK 64
.DATA
 LIST DB 12,-14,16,18,-12,0
 MSG1 DB "THERE ARE $"
 PCOUNT DB 30H
 MSG2 DB "POSITIVE VALUES AND $"
 NCOUNT DB 30H
 MSG3 DB " NEGATIVE VALUES IN THE LIST.$"
.CODE
MAIN PROC
 MOV AX,@DATA
 MOV DS,AX
 
 MOV SI,0
 CHK:
  CMP LIST[SI],0
  JE PRINT
  JG CALCP
  JL CALCN 

 CALCP:
  INC PCOUNT 
  INC SI 
  JMP CHK

 CALCN:
  INC NCOUNT 
  INC SI 
  JMP CHK



 PRINT:
    ;-OUTPUT STRING:09H(MSG1)
    MOV AH,09H
    LEA DX,MSG1
    INT 21H
    ;-OUTPUT BYTE:02H(PCOUNT)
    MOV AH,02H
    MOV DL,PCOUNT 
    INT 21H
    ;-OUTPUT STRING:09H(MSG2)
    MOV AH,09H
    LEA DX,MSG2
    INT 21H
    ;-OUTPUT BYTE:02H(NCOUNT)
    MOV AH,02H
    MOV DL,NCOUNT 
    INT 21H

    ;-OUTPUT STRING:09H(MSG3)
    MOV AH,09H
    LEA DX,MSG3
    INT 21H
    



 MOV AH,4CH
 INT 21H
MAIN ENDP
 END MAIN