.MODEL SMALL
.STACK 64
.DATA
.CODE
 FIB DB 1,1,0,0,0,0
MAIN PROC
 MOV AX,@DATA
 MOV DS,AX
 
 MOV CX,4
 MOV SI,1
 MOV DI,0
 MOV BX,2
 CALC:
  MOV AL,FIB[SI]
  ADD AL,FIB[DI]
  MOV FIB[BX],AL
  INC SI
  INC DI
  INC BX
  LOOP CALC

 MOV CX,6
 MOV SI,0
 PRINT:
  ADD FIB[SI],30H
  MOV AH,02H
  MOV DL,FIB[SI]
  INT 21H
  MOV AH,02H
  MOV DL,','
  INT 21H
  MOV AH,02H
  MOV DL,' '
  INT 21H
  INC SI
  LOOP PRINT 
  
  


 MOV AH,4CH
 INT 21H
MAIN ENDP
 END MAIN